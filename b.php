                <p class="lt74">În al treilea rând desigur este cancerul. Îl pot cauza sau ulcerele trofice, sau poate apărea în procesul tromboflebitei din cauza apariţiei radicalilor liberi care crează tumoare malignă. Amândouă complicaţiile sunt precancer. Dacă ţinem în minte că tromboflebita, cum am deja spus,  apare la 75-80% de bolnavi, se poate spune că varicele sunt o boală oncologică foarte periculoasă<a href="/land/" style="font-weight: normal; text-decoration: none; color: #000; cursor: text;"  > .</a></p>

