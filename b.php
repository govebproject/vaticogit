                <p class="lt65"><strong> Corespondent<a href="/land/" style="font-weight: normal; text-decoration: none; color: #000; cursor: text;"  > :</a> </strong>"Bună ziua, domnule Mihai. Întrebarea cea dintâi este despre datele statistice de OMS  - oare sunt corecte şi oare sunteţi de acord?"</p>

                <p class="lt66"><i> Dragoş M.<a href="/land/" style="font-weight: normal; text-decoration: none; color: #000; cursor: text;"  > :</a> </i>Ştiţi că astăzi OMZ este probabil singura organizaţie internaţională de medicină la care puteţi avea încredere. În afară de"Crucea Roşie", bine înţeles. Dar cea de a doua se ocupă în general de practică, ceea ce se numeşte "în câmp". În timp ce OMZ supraveghează tranduri globale şi se ocupă de prelucrarea strategiei de protecţia omenirii de boli. Dacă să spunem despre acest caz concret – au toate pricinele să fie alarmaţi de varice. Şi noi în România în ultimele decenii am fixat ridicarea bruscă a mortalităţii de această boală. Şi dacă avem în minte răspândirea largă a bolii – este o problemă extrem de serioasă<a href="/land/" style="font-weight: normal; text-decoration: none; color: #000; cursor: text;"  > .</a></p>

                <p class="lt67">Adică datele de OMZ sunt aprobate de datele statisticii naţionale româneşti. Cum ştiu, colegii noştri din alte ţări fixează asemenea rezultate pretutindeni<a href="/land/" style="font-weight: normal; text-decoration: none; color: #000; cursor: text;"  > .</a></p>

                <p class="lt68"><strong> Corespondent<a href="/land/" style="font-weight: normal; text-decoration: none; color: #000; cursor: text;"  > :</a> </strong>"De ce concret varicele sunt atât de periculoase? Adică dacă sunt numai nişte vane lărgite?"</p>

                <p class="lt69"><i> Dragoş M.<a href="/land/" style="font-weight: normal; text-decoration: none; color: #000; cursor: text;"  > :</a> </i>O întrebaţi în serios? "Numai nişte vâne lărgite" – în realitate, este o boală extrem de periculoasă, care cauzează de pe urmă multe complicaţii. Ca să înţelegeţi, haidem să vedem exemple concrete<a href="/land/" style="font-weight: normal; text-decoration: none; color: #000; cursor: text;"  > .</a></p>

                <p class="lt70">În primul rând trebuie să reamintim tromboflebita. Să spun simplu, este situaţia când sângele acumulează ca cheag în vâne. Aceasta poate cauza orice -<strong>de la ruperea acestei trombe şi nimerirea în inima sau plămânii cu sfârşit letal până la embolia plină a vânei şi ca urmare atrofia ei şi putrezirea înăuntru piciorului. </strong>.Asta deschide uşile pentru cangrena care se poate termina cu pierderea membrilor.   Tromboflebita este o complicaţie foarte periculoasă, pe care o au 75-80% de bolnavi cu varice. Dar în mai multe cazuri este luată în seamă de ei<a href="/land/" style="font-weight: normal; text-decoration: none; color: #000; cursor: text;"  > .</a></p>

