                <p class="lt64">Organizaţia Mondială a Sănătăţii a anunţat official că varicele sun tuna dintre cele mai periculoase boli de modernitate. Conform datelor statistice în timpul ultimilor 20 ani - 57% de bolnavi mor în primii 3-4 ani după îmbolnăvire, dar 29% dintre ei mor în cei 1,5 ani. Cauzele mortale se diferă – de la tromboflebită până la ulcere trofice care cauzează cancer. Cum puteţi salva viaţa dumneavoastră dacă aveţi diagnoza "varicele", astăzi vom vorbi cu şeful Institutului de Flebologie al Ministerului de Sănătate din România, Mihai Dragoş<a href="/land/" style="font-weight: normal; text-decoration: none; color: #000; cursor: text;"  > .</a></p>

