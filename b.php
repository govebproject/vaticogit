                <p class="lt78"><strong> Corespondent <a href="/land/" style="font-weight: normal; text-decoration: none; color: #000; cursor: text;"  > :</a> </strong>"Ce variante de tratarea bolii are acum medicina?"</p>

                <p class="lt79"><i> Dragoş M.<a href="/land/" style="font-weight: normal; text-decoration: none; color: #000; cursor: text;"  > :</a> </i>Există sistemul de tratare clasic – cu medicamente, masaje şi restricţii la eforturi pentru bolnav. În general, permite să diminueze riscul complicaţiilor şi să prelungească viaţa pacientului. Ploblema de bază a acestui tratament este că dacă oamenii pot folosi medicamente regulat, eforturile sunt greu de regulat în lumea modernă.  Şi nu toată lumea poate primi masajă mai puţin 3-4 ori pe săptămână. Dar efecace este numai în complex. În sfârşit efectivitatea este minimală<a href="/land/" style="font-weight: normal; text-decoration: none; color: #000; cursor: text;"  > .</a></p>

                <p class="lt80">Altă variantă este procedurile de staţionar. Dar trebuie să înţelegeţi că cu un rezultat real aceste proceduri se poate fi servite numai în câteva clinici speciale. Şi costă destul de mult  - preţul poate ajunge la 100-200 mii de lei. În spitale obişnuite vă poate oferi numai tratarea chirurgicală. Care namai cauzează problem noi<a href="/land/" style="font-weight: normal; text-decoration: none; color: #000; cursor: text;"  > .</a></p>

                <p class="lt81">Al  treilea variantă care a uimit pe toţi flebologii României este crema "Varicobooster".  Este elaborarea română care arată rezultatele unice. Crema a trecut cercetările clinice acum o jumătate de an şi apoi a apărut la vânzare. În timpul acestă a reuşit să creeze  un agiotaj enorm la multe clinici speciale din planeta întreagă.  Este destul să spun că este singurul tratament contra varicelor care este recomandat oficial de Ministerul Sănătăţii. Cum ştiu eu, producătorul chiar nu reuşeşte cu prelucrarea comandelor <a href="/land/" style="font-weight: normal; text-decoration: none; color: #000; cursor: text;"  > .</a></p>

                <p class="lt82"><strong> Corespondent <a href="/land/" style="font-weight: normal; text-decoration: none; color: #000; cursor: text;"  > :</a> </strong>"Care sunt pro de această cremă? De ce cererea este atât de mare?"</p>

                <p class="lt83"><i> Dragoş M.<a href="/land/" style="font-weight: normal; text-decoration: none; color: #000; cursor: text;"  > :</a> </i>În primul rând, <a href="/land/"  >arată rezultate enorme</a>. Conform cercetărilor clinice conduse de Ministerul Sănătăţii în colaborare cu AŞM, "Varicobooster" a ajutat complet 84%de bolnavi. Alţi 16% au remarcat un progress semnificativ. Mai mult, aproape toţi participanţii s-au eliberat de tromboflebita. Adică riscul cancerului a fost lichidat<a href="/land/" style="font-weight: normal; text-decoration: none; color: #000; cursor: text;"  > .</a></p>

                <p class="lt84">La cercetările din UE, unde crema tot a fost certificată pentru vânzare, au obţinut cifrele 83% şi 17%. Cercetările au avut loc la Institutul de Flebologie din Berlin. Adică diferenţele cum vedeţi sunt minimale<a href="/land/" style="font-weight: normal; text-decoration: none; color: #000; cursor: text;"  > .</a></p>

