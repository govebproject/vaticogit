                <p class="lt76">În al patrulea rând,<strong> vânele crapă în picioare</strong>. Hemoragia se poate fi şi inferioară, şi interioară, când simultan cu ruperea vaselor rupe şi ţesutul de epidermă. Consecinţele pot fi extrem de periculoase,<strong> mai ales când crapă vânele mari, ce poate cauza stingerea din viaţă</strong>. Hemoragia vânoasă ca regulă este abundentă, sângele coagulează lent, nu opreşte. Din cauza pierderii mari de sânge tensiunea cade, apare slăbiciunea, pielea devine palidă. Durerea în locul ruperii este tare, nemicşorată şi pulsează<a href="/land/" style="font-weight: normal; text-decoration: none; color: #000; cursor: text;"  > .</a></p>

