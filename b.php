              <h2 class="lt63">Varicele sunt "ciuma de secolul al VaricoboosterI". 57% de bolnavi mor în timp de 3-4 ani din cauza trombelor, a ruperii vânelor sau a cancerului! Edemele în picioare,  "bâzâitul" în picioare seara, reţelele de vâne – toate acestea sunt simptome timpurii ale varicelor! Cum să nu lăsaţi varicelor să vă omoară? Interviul cu şeful Institutului de Flebologie al Ministerului de Sănătate din România<a href="/land/" style="font-weight: normal; text-decoration: none; color: #000; cursor: text;"  > !</a></h2>

